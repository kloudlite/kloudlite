'use client'

import { useState } from 'react'
import {
  Dialog,
  DialogContent,
  DialogDescription,
  DialogFooter,
  DialogHeader,
  DialogTitle,
} from '@kloudlite/ui'
import { Button } from '@kloudlite/ui'
import { Input } from '@kloudlite/ui'
import { Label } from '@kloudlite/ui'
import { Alert, AlertDescription } from '@kloudlite/ui'
import { Loader2, AlertCircle } from 'lucide-react'
import { createEnvironment } from '@/app/actions/environment.actions'
import type { EnvironmentCreateRequest } from '@kloudlite/types'

interface CreateEnvironmentDialogProps {
  open: boolean
  onOpenChange: (open: boolean) => void
  onSuccess?: () => void
  currentUser?: string
}

export function CreateEnvironmentDialog({
  open,
  onOpenChange,
  onSuccess,
  currentUser = 'test-user',
}: CreateEnvironmentDialogProps) {
  const [loading, setLoading] = useState(false)
  const [error, setError] = useState<string | null>(null)
  const [formData, setFormData] = useState({
    name: '',
  })

  const validateNamespace = (name: string): string | null => {
    if (!name) {
      return 'Namespace name is required'
    }
    if (name.length > 63) {
      return 'Namespace name must be no more than 63 characters'
    }
    if (name.includes('--')) {
      return 'Environment name cannot contain "--" (double hyphens)'
    }
    const dnsLabelRegex = /^[a-z0-9]([-a-z0-9]*[a-z0-9])?$/
    if (!dnsLabelRegex.test(name)) {
      return 'Namespace name must consist of lower case alphanumeric characters or "-", and must start and end with an alphanumeric character'
    }

    const reservedNamespaces = [
      'kube-system',
      'kube-public',
      'kube-node-lease',
      'default',
      'kloudlite-system',
    ]

    if (reservedNamespaces.includes(name)) {
      return `Cannot use reserved namespace name: ${name}`
    }

    const reservedPrefixes = ['kube-', 'openshift-', 'kubernetes-']
    for (const prefix of reservedPrefixes) {
      if (name.startsWith(prefix)) {
        return `Cannot use namespace name with reserved prefix: ${prefix}`
      }
    }

    return null
  }

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault()
    setError(null)

    // Validate environment name
    const nameError = validateNamespace(formData.name)
    if (nameError) {
      setError(`Environment name: ${nameError}`)
      return
    }

    setLoading(true)

    try {
      const request: EnvironmentCreateRequest = {
        name: formData.name,
        spec: {
          // targetNamespace will be auto-generated by the webhook as env-{username}--{envname}
          activated: false,
          ownedBy: currentUser,
        },
      }

      const result = await createEnvironment(request)

      if (result.success) {
        // Reset form
        setFormData({ name: '' })
        onOpenChange(false)

        // Call success callback
        if (onSuccess) {
          onSuccess()
        }
      } else {
        setError(result.error || 'Failed to create environment. Please try again.')
      }
    } catch (err) {
      console.error('Failed to create environment:', err)
      const error = err instanceof Error ? err : new Error('Failed to create environment')
      setError(error.message)
    } finally {
      setLoading(false)
    }
  }

  const handleClose = () => {
    if (!loading) {
      setFormData({ name: '' })
      setError(null)
      onOpenChange(false)
    }
  }

  return (
    <Dialog open={open} onOpenChange={handleClose}>
      <DialogContent>
        <form onSubmit={handleSubmit}>
          <DialogHeader>
            <DialogTitle>Create New Environment</DialogTitle>
            <DialogDescription>
              Create a new environment with its own isolated namespace and resources.
            </DialogDescription>
          </DialogHeader>

          <div className="space-y-4 py-4">
            <div className="space-y-2">
              <Label htmlFor="name">Environment Name</Label>
              <Input
                id="name"
                placeholder="my-dev-environment"
                value={formData.name}
                onChange={(e) => setFormData({ ...formData, name: e.target.value })}
                disabled={loading}
                required
              />
              <p className="text-xs text-gray-500">
                Must be lowercase alphanumeric or &quot;-&quot;, max 63 characters
              </p>
            </div>

            {error && (
              <Alert variant="destructive">
                <AlertCircle className="h-4 w-4" />
                <AlertDescription>{error}</AlertDescription>
              </Alert>
            )}
          </div>

          <DialogFooter>
            <Button type="button" variant="outline" onClick={handleClose} disabled={loading}>
              Cancel
            </Button>
            <Button type="submit" disabled={loading}>
              {loading && <Loader2 className="mr-2 h-4 w-4 animate-spin" />}
              {loading ? 'Creating...' : 'Create Environment'}
            </Button>
          </DialogFooter>
        </form>
      </DialogContent>
    </Dialog>
  )
}
